# can we take the value from user?
a = int(input("Enter the value of a\n")) # 10
b = int(input("Enter the value of b\n")) # 3

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a**b) #10^3
print(a//b) # 10/3 0 -> 3.3 -> int -> 3 Q
print(a%b) 
