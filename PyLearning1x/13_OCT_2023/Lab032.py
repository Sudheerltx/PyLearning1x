# Assignment Operators:
# = Assignment
# += Add and assign
# -= Subtract and assign
# *= Multiply and assign
# /= Divide and assign
# %= Modulus and assign
# **= Exponentiate and assign
# //= Floor divide and assign

a = 5
a+=2 # a = a+2 increase by 2
print(a)

a-=1 # a= a-1
print(a)
a*=3 # a = a*3
print(a) # 18

a/=2 # 9
print(a) # 9
a%=4  # 9%4 -> 1
print(a)

